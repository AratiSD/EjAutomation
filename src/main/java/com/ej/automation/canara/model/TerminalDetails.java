package com.ej.automation.canara.model;

public class TerminalDetails {
	private String terminalseqno;
	private String terminalId;
	private String bankId;
	private String deviceType;
	private String termName;
	private String termIP;
	public String getTerminalseqno() {
		return terminalseqno;
	}
	public void setTerminalseqno(String terminalseqno) {
		this.terminalseqno = terminalseqno;
	}
	public String getTerminalId() {
		return terminalId;
	}
	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}
	public String getBankId() {
		return bankId;
	}
	public void setBankId(String bankId) {
		this.bankId = bankId;
	}
	public String getDeviceType() {
		return deviceType;
	}
	public void setDeviceType(String deviceType) {
		this.deviceType = deviceType;
	}
	public String getTermName() {
		return termName;
	}
	public void setTermName(String termName) {
		this.termName = termName;
	}
	public String getTermIP() {
		return termIP;
	}
	public void setTermIP(String termIP) {
		this.termIP = termIP;
	}
}
